const corrupt = [];
const incomplete = [];

const openers = ['(', '[', '{', '<'];
const closers = [')', ']', '}', '>'];
const cPoints = [3, 57, 1197, 25137];

function scan(line) {
    const thisLine = line.split('');
    let pos = 0;
    const stack = [];
    while (pos < thisLine.length) {
        const char = thisLine[pos];
        if (openers.includes(char)) {
            stack.push(char);
        } else if (closers.includes(char)) {
            const last = stack.pop();
            if (openers.indexOf(last) !== closers.indexOf(char)) {
                corrupt.push(cPoints[closers.indexOf(char)]);
                return;
            }
        }
        pos++;
    }
    let score = 0;
    stack.reverse();
    while (stack.length > 0) {
        const char = stack.pop();
        score *= 5;
        score += openers.indexOf(char) + 1;
    }
    incomplete.push(stack);
}

parsed.forEach((line) => scan(line));

