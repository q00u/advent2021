function enhance(oldInImage, padType) {
    const inImage = padImage(oldInImage, padType);
    const newImage = Array.from(Array(inImage.length), () => Array(inImage[0].length).fill(0));
    for (let i = 1; i < inImage.length - 1; i++) {
        for (let j = 1; j < inImage[i].length - 1; j++) {
            const top = inImage[i - 1].slice(j - 1, j + 2).join('');
            const mid = inImage[i].slice(j - 1, j + 2).join('');
            const bot = inImage[i + 1].slice(j - 1, j + 2).join('');
            // console.log(top, mid, bot);
            const address = parseInt(top + mid + bot, 2);
            // console.log(address);
            newImage[i][j] = enhancement[address];
            // console.log(i, j, top, mid, bot, address, enhancement[address]);
        }
    }
    return cropImage(newImage);
}

