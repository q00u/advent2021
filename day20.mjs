function padImage(inImage, padType) {
    const padSize = 3;
    const newImage = [];
    const padVal = (padType + 1) % 2 === 0 ? 1 : 0;
    // console.log('Padding with', padVal);
    inImage.forEach((row) => {
        newImage.push([...Array(padSize).fill(padVal), ...row, ...Array(padSize).fill(padVal)]);
    });
    const newLine = [...Array(padSize).fill(padVal), ...Array(inImage[0].length).fill(padVal), ...Array(padSize).fill(padVal)];
    for (let i = 0; i < padSize; i++) {
        newImage.unshift(newLine);
        newImage.push(newLine);
    }
    // console.log('Padded image:');
    // printImage(newImage);
    return newImage;
}

