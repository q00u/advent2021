function cropImage(inImage) {
    const cropSize = 2;
    const newImage = [];
    inImage.forEach((row) => {
        newImage.push(row.slice(cropSize, row.length - cropSize));
    });
    for (let i = 0; i < cropSize; i++) {
        newImage.shift();
        newImage.pop();
    }
    // console.log('Cropped image:');
    // printImage(newImage);
    return newImage;
}

